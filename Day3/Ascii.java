package Day3;

public class Ascii {
	
	public static void main (String[] args) {
		
		for(char i='A'; i<='Z'; i++) {
			int j = i;
			System.out.print(i +" "+j +" ");
		}
		
		System.out.println("");
		
		for(char i='a'; i<='z'; i++) {
			int j = i;
			System.out.print(i +" "+j +" ");
		}
		
	}
	
}
